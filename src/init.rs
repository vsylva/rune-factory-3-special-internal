#[inline]
pub(crate) unsafe fn on_game_start(h_module: isize) {
    let trainer = Trainer {
        显示界面: true,
        选择的作物: Crop::无_0,
        作物列表: &[],
        选择的作物等级: CropLevel::LV1,
        作物等级列表: &[],
        选择的作物阶段: CropStage::一阶段,
        作物阶段列表: &[],
        选择的秒: 0,
        秒列表: Vec::new(),
        选择的时: 0,
        时列表: Vec::new(),
        选择的天: 1,
        天列表: Vec::new(),
        选择的季节: Season::Spring,
        季节列表: &[],
        选择的年: 1,
        年列表: Vec::new(),
        选择的流速: TimeSpeed::Normal,
        时间流速列表: &[],

        选择的物品: Item::卷心菜_1,
        物品列表: &[],
        选择的物品数量: 1,
        物品数量: Vec::new(),
    };

    let time_begin = ::std::time::Instant::now();
    let time_one_sec = ::core::time::Duration::from_secs(1);

    while time_begin.elapsed().as_secs() < 120 {
        ::std::thread::sleep(time_one_sec);

        if let Some(live2d) = libmem::find_module("Live2DCubismCore.dll")
            && live2d.base != 0
            && let Some(sandll) = libmem::find_module("SanDLL.dll")
            && sandll.base != 0
        {
            GLOBAL_SANDLL_INFO = sandll;
            break;
        }
    }

    if ::hudhook::Hudhook::builder()
        .with::<hudhook::hooks::dx11::ImguiDx11Hooks>(trainer)
        .with_hmodule(hudhook::windows::Win32::Foundation::HINSTANCE(h_module))
        .build()
        .apply()
        .is_err()
    {
        ::hudhook::eject();
    }
}

#[inline]
pub(crate) unsafe fn on_ui_start<'a>(
    trainer: &'a mut Trainer,
    ctx: &mut hudhook::imgui::Context,
    _: &'a mut dyn hudhook::RenderContext,
) {
    init_hooks(
        crate::GLOBAL_SANDLL_INFO.base,
        crate::GLOBAL_SANDLL_INFO.size,
    );

    init_vals(trainer);

    ctx.set_ini_filename(None);

    ctx.style_mut().use_light_colors();

    hudhook::imgui::sys::ImFontAtlas_AddFontFromFileTTF(
        hudhook::imgui::internal::RawCast::raw_mut(ctx.fonts()),
        "C:\\windows\\fonts\\simhei.ttf\0".as_ptr().cast(),
        20.0,
        ::core::ptr::null(),
        hudhook::imgui::sys::ImFontAtlas_GetGlyphRangesChineseFull(
            hudhook::imgui::internal::RawCast::raw_mut(ctx.fonts()),
        ),
    );
}

